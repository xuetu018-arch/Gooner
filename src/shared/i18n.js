  function normalizeSupportedLanguage(value) {
    const text = String(value || '').trim().toLowerCase();
    if (!text) {
      return null;
    }
    if (text.startsWith('zh')) {
      return 'zh-CN';
    }
    if (text.startsWith('en')) {
      return 'en-US';
    }
    return null;
  }

  function resolveLanguage(value, fallback = 'zh-CN') {
    if (value === 'system' || !value) {
      return normalizeSupportedLanguage(fallback) || 'zh-CN';
    }
    return normalizeSupportedLanguage(value) || normalizeSupportedLanguage(fallback) || 'zh-CN';
  }

  function getDictionary(locale) {
    return dictionaries[resolveLanguage(locale)] || dictionaries['zh-CN'];
  }

  function interpolate(template, params) {
    return String(template).replace(/\{(\w+)\}/g, (_match, key) => {
      if (!params || params[key] === undefined || params[key] === null) {
        return '';
      }
      return String(params[key]);
    });
  }

  function translate(locale, key, params) {
    const dictionary = getDictionary(locale);
    const fallbackDictionary = dictionaries['zh-CN'];
    const template = dictionary[key] ?? fallbackDictionary[key] ?? key;
    return interpolate(template, params);
  }

  return {
    dictionaries,
    resolveLanguage,
    translate
  };
